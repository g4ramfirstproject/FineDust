package com.example.ju.finedust;

public class AreaInfo {
    String areaName;
    String fineDustConcentration;
    String fineDustGrade;

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public String getFineDustConcentration() {
        return fineDustConcentration;
    }

    public void setFineDustConcentration(String fineDustConcentration) {
        this.fineDustConcentration = fineDustConcentration;
    }

    public String getFineDustGrade() {
        return fineDustGrade;
    }

    public void setFineDustGrade(String fineDustGrade) {
        this.fineDustGrade = fineDustGrade;
    }
}
